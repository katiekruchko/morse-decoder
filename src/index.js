const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let arr = expr.match(/.{10}/g);

    arr.forEach(function (item, el) {
        arr[el] = item.replace(/10/gi, '.').replace(/11/gi, '-').replace(/0/gi, '').replace('**********', ' ');
    });

    arr.forEach(function (item, el) {
        if (item in MORSE_TABLE) {
            arr[el] = MORSE_TABLE[item];
        }
    });

    let result = arr.join('');
    return (result);
}



module.exports = {
    decode
}
